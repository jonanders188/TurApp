import fs from 'node:fs';

const trailsPath = new URL('../data/trails.vestfold.json', import.meta.url);
const outPath = new URL('../supabase/seed.sql', import.meta.url);
const trails = JSON.parse(fs.readFileSync(trailsPath, 'utf8'));

const esc = (value) => String(value).replaceAll("'", "''");
const sqlBool = (value) => value ? 'true' : 'false';

const rows = trails.map((t) => `(
  '${esc(t.id)}',
  '${esc(t.name)}',
  '${esc(t.municipality)}',
  '${esc(t.area)}',
  ${t.latitude},
  ${t.longitude},
  ${t.distance_km},
  ${t.estimated_minutes},
  '${esc(t.difficulty)}',
  '${esc(t.route_type)}',
  '${esc(t.surface_type)}',
  ${t.elevation_gain_m},
  ${sqlBool(t.suitable_stroller)},
  ${sqlBool(t.suitable_baby_carrier)},
  ${sqlBool(t.suitable_wheelchair)},
  ${sqlBool(t.suitable_easy_walk)},
  ${sqlBool(t.suitable_children)},
  ${sqlBool(t.suitable_dog)},
  ${sqlBool(t.has_parking)},
  ${sqlBool(t.has_toilet)},
  ${sqlBool(t.has_bench)},
  ${sqlBool(t.has_viewpoint)},
  ${sqlBool(t.has_cafe_nearby)},
  '${esc(t.image_url)}',
  ARRAY[${t.tags.map((tag) => `'${esc(tag)}'`).join(', ')}],
  '${esc(t.description)}',
  '${esc(t.data_quality_note ?? '')}',
  '${esc(JSON.stringify({ type: 'LineString', coordinates: t.route_coordinates }))}'::jsonb
)`).join(',\n');

const sql = `-- Generated by scripts/generate-supabase-seed.mjs
-- Run after supabase/schema.sql

insert into public.trails (
  id, name, municipality, area, latitude, longitude, distance_km, estimated_minutes,
  difficulty, route_type, surface_type, elevation_gain_m,
  suitable_stroller, suitable_baby_carrier, suitable_wheelchair, suitable_easy_walk,
  suitable_children, suitable_dog, has_parking, has_toilet, has_bench, has_viewpoint,
  has_cafe_nearby, image_url, tags, description, data_quality_note, route_geojson
) values
${rows}
on conflict (id) do update set
  name = excluded.name,
  municipality = excluded.municipality,
  area = excluded.area,
  latitude = excluded.latitude,
  longitude = excluded.longitude,
  distance_km = excluded.distance_km,
  estimated_minutes = excluded.estimated_minutes,
  difficulty = excluded.difficulty,
  route_type = excluded.route_type,
  surface_type = excluded.surface_type,
  elevation_gain_m = excluded.elevation_gain_m,
  suitable_stroller = excluded.suitable_stroller,
  suitable_baby_carrier = excluded.suitable_baby_carrier,
  suitable_wheelchair = excluded.suitable_wheelchair,
  suitable_easy_walk = excluded.suitable_easy_walk,
  suitable_children = excluded.suitable_children,
  suitable_dog = excluded.suitable_dog,
  has_parking = excluded.has_parking,
  has_toilet = excluded.has_toilet,
  has_bench = excluded.has_bench,
  has_viewpoint = excluded.has_viewpoint,
  has_cafe_nearby = excluded.has_cafe_nearby,
  image_url = excluded.image_url,
  tags = excluded.tags,
  description = excluded.description,
  data_quality_note = excluded.data_quality_note,
  route_geojson = excluded.route_geojson;
`;

fs.writeFileSync(outPath, sql);
console.log(`Wrote ${outPath.pathname}`);
